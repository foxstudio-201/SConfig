import { CHECK_CATALOG_UNIQUE } from './lpxData'

function indent(level) {
  return '  '.repeat(level)
}

function quoteStr(s) {
  const str = String(s ?? '')
  if (str.includes("'") && !str.includes('"')) return `"${str}"`
  if (str.includes('"')) return `'${str.replace(/'/g, "''")}'`
  return `'${str}'`
}

function formatScalar(v) {
  if (v === true) return 'true'
  if (v === false) return 'false'
  if (typeof v === 'number') return String(v)
  const s = String(v ?? '').trim()
  if (s === '') return "''"
  if (/^-?\d+(\.\d+)?$/.test(s)) return s
  return quoteStr(s)
}

function writeStringList(lines, items, level) {
  ;(items || []).forEach(item => {
    const t = String(item).trim()
    if (t) lines.push(`${indent(level)}- ${formatScalar(t)}`)
  })
}

function writeOptions(lines, options, level) {
  if (!options || typeof options !== 'object') return
  Object.entries(options).forEach(([key, val]) => {
    if (Array.isArray(val)) {
      lines.push(`${indent(level)}${key}:`)
      writeStringList(lines, val, level + 1)
    } else if (val !== null && typeof val === 'object') {
      lines.push(`${indent(level)}${key}:`)
      writeOptions(lines, val, level + 1)
    } else {
      lines.push(`${indent(level)}${key}: ${formatScalar(val)}`)
    }
  })
}

function writeCheckBlock(lines, chk, level) {
  lines.push(`${indent(level)}enabled: ${formatScalar(chk.enabled)}`)
  lines.push(`${indent(level)}punish: ${formatScalar(chk.punish)}`)
  lines.push(`${indent(level)}max-vl: ${formatScalar(chk.maxVl)}`)
  lines.push(`${indent(level)}min-vl: ${formatScalar(chk.minVl)}`)
  lines.push(`${indent(level)}punish-commands:`)
  writeStringList(lines, chk.punishCommands, level + 1)
  if (chk.buffer) {
    lines.push(`${indent(level)}buffer:`)
    lines.push(`${indent(level + 1)}max: ${formatScalar(chk.buffer.max)}`)
    lines.push(`${indent(level + 1)}multiply: ${formatScalar(chk.buffer.multiply)}`)
    lines.push(`${indent(level + 1)}decay: ${formatScalar(chk.buffer.decay)}`)
  }
  if (chk.options && Object.keys(chk.options).length > 0) {
    lines.push(`${indent(level)}options:`)
    writeOptions(lines, chk.options, level + 1)
  }
}

function buildChecksSection(s) {
  const tree = {}
  CHECK_CATALOG_UNIQUE.forEach(meta => {
    const chk = s.checks?.[meta.id]
    if (!chk) return
    if (!tree[meta.category]) tree[meta.category] = {}
    tree[meta.category][meta.type] = chk
  })

  const out = ['checks:']
  Object.entries(tree).forEach(([cat, types]) => {
    out.push(`${indent(1)}${cat}:`)
    Object.entries(types).forEach(([type, chk]) => {
      out.push(`${indent(2)}${type}:`)
      writeCheckBlock(out, chk, 3)
      out.push('')
    })
  })
  return out.join('\n').trimEnd()
}


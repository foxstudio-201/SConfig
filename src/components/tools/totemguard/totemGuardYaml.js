function buildCheck(lines, name, check) {
  lines.push(`${name}:`)
  lines.push(`${indent(1)}enabled: ${formatScalar(check.enabled)}`)
  lines.push(`${indent(1)}punishable: ${formatScalar(check.punishable)}`)
  lines.push(`${indent(1)}punishment-delay-in-seconds: ${formatScalar(check.punishmentDelayInSeconds)}`)
  lines.push(`${indent(1)}max-violations: ${formatScalar(check.maxViolations)}`)
  lines.push(`${indent(1)}punishment-commands:`)
  writeStringList(lines, check.punishmentCommands, 2)

  const baseKeys = ['enabled', 'punishable', 'punishmentDelayInSeconds', 'maxViolations', 'punishmentCommands']
  Object.entries(check).forEach(([key, value]) => {
    if (baseKeys.includes(key)) return
    const yamlKey = key
      .replace(/[A-Z]/g, m => `-${m.toLowerCase()}`)
      .replace('s-d', 'sd')
    if (Array.isArray(value)) {
      lines.push(`${indent(1)}${yamlKey}:`)
      writeStringList(lines, value, 2)
    } else {
      lines.push(`${indent(1)}${yamlKey}: ${formatScalar(value)}`)
    }
  })
  lines.push('')
}

export function buildChecksYaml(s) {
  const lines = [
    '# TotemGuard checks — generated by SConfig',
    '',
    `default-punishment: ${formatScalar(s.defaultPunishment)}`,
    '',
  ]
  Object.entries(s.map || {}).forEach(([name, check]) => buildCheck(lines, name, check))
  return lines.join('\n')
}

export function buildMessagesYaml(s) {
  const lines = [
    '# TotemGuard messages — generated by SConfig',
    '',
    `format: ${formatScalar(s.format)}`,
    `prefix: ${formatScalar(s.prefix)}`,
    '',
    'alert-format:',
    `${indent(1)}alert-format: ${formatScalar(s.alertFormat?.alertFormat)}`,
    `${indent(1)}alert-format-console: ${formatScalar(s.alertFormat?.alertFormatConsole)}`,
    `${indent(1)}alert-hover-message: |`,
  ]
  String(s.alertFormat?.alertHoverMessage || '').split('\n').forEach(line => {
    lines.push(`${indent(2)}${line}`)
  })
  lines.push(
    `${indent(1)}alert-click-command: ${formatScalar(s.alertFormat?.alertClickCommand)}`,
    `${indent(1)}dev-prefix: ${formatScalar(s.alertFormat?.devPrefix)}`,
    `${indent(1)}check-details-color:`,
    `${indent(2)}main: ${formatScalar(s.alertFormat?.checkDetailsColor?.main)}`,
    `${indent(2)}secondary: ${formatScalar(s.alertFormat?.checkDetailsColor?.secondary)}`,
    '',
    `alerts-enabled: ${formatScalar(s.alertsEnabled)}`,
    `alerts-disabled: ${formatScalar(s.alertsDisabled)}`,
    `alerts-enabled-other: ${formatScalar(s.alertsEnabledOther)}`,
    `alerts-disabled-other: ${formatScalar(s.alertsDisabledOther)}`,
    `no-permission-toggle-other: ${formatScalar(s.noPermissionToggleOther)}`,
    `alert-toggle-blocked: ${formatScalar(s.alertToggleBlocked)}`,
    `alert-brand: ${formatScalar(s.alertBrand)}`,
    '',
    'command-messages:',
  )
  writeMap(lines, s.commandMessages || {}, 1)
  lines.push('')
  return lines.join('\n')
}

function writeWebhook(lines, key, v) {
  lines.push(`${key}:`)
  lines.push(`${indent(1)}enabled: ${formatScalar(v.enabled)}`)
  lines.push(`${indent(1)}url: ${formatScalar(v.url)}`)
  lines.push(`${indent(1)}name: ${formatScalar(v.name)}`)
  lines.push(`${indent(1)}color: ${formatScalar(v.color)}`)
  lines.push(`${indent(1)}title: ${formatScalar(v.title)}`)
  lines.push(`${indent(1)}profile-image: ${formatScalar(v.profileImage)}`)
  lines.push(`${indent(1)}timestamp: ${formatScalar(v.timestamp)}`)
  lines.push(`${indent(1)}footer: ${formatScalar(v.footer)}`)
  lines.push('')
}

export function buildWebhooksYaml(s) {
  const lines = ['# TotemGuard webhooks — generated by SConfig', '']
  writeWebhook(lines, 'alert', s.alert || {})
  writeWebhook(lines, 'punishment', s.punishment || {})
  return lines.join('\n')
}

export function downloadYaml(content, filename) {
  const blob = new Blob([content], { type: 'text/yaml' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  a.click()
  URL.revokeObjectURL(url)
}

function indent(level) {
  return '  '.repeat(level)
}

function quoteStr(s) {
  const str = String(s ?? '')
  if (str.includes("'") && !str.includes('"')) return `"${str}"`
  if (str.includes('"')) return `'${str.replace(/'/g, "''")}'`
  return `'${str}'`
}

function formatScalar(v) {
  if (v === true) return 'true'
  if (v === false) return 'false'
  if (typeof v === 'number') return String(v)
  const s = String(v ?? '').trim()
  if (s === '') return "''"
  if (/^-?\d+(\.\d+)?$/.test(s)) return s
  return quoteStr(s)
}

function writeStringList(lines, list, level) {
  ;(list || []).forEach(item => {
    const t = String(item ?? '').trim()
    if (t) lines.push(`${indent(level)}- ${formatScalar(t)}`)
  })
}

function writeMap(lines, map, level) {
  Object.entries(map || {}).forEach(([key, value]) => {
    if (Array.isArray(value)) {
      lines.push(`${indent(level)}${key}:`)
      writeStringList(lines, value, level + 1)
      return
    }
    if (value && typeof value === 'object') {
      lines.push(`${indent(level)}${key}:`)
      writeMap(lines, value, level + 1)
      return
    }
    lines.push(`${indent(level)}${key}: ${formatScalar(value)}`)
  })
}


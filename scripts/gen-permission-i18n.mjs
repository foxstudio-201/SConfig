/** @param {string} node */
export function permNodeKey(node) {
  if (node === '*') return 'wildcard'
  return node.replace(/\./g, '_').replace(/\*/g, 'star')
}

// Parse PERMISSION_CATEGORIES from permissionData.js (eval export block)
const dataSrc = fs.readFileSync(path.join(root, 'src/components/tools/permissionData.js'), 'utf8')
const start = dataSrc.indexOf('export const PERMISSION_CATEGORIES = ')
const end = dataSrc.indexOf('\nexport const ALL_PRESET_NODES')
if (start < 0 || end < 0) throw new Error('Could not parse PERMISSION_CATEGORIES')
const arrSrc = dataSrc.slice(start + 'export const PERMISSION_CATEGORIES = '.length, end)
// eslint-disable-next-line no-eval
const PERMISSION_CATEGORIES = eval(arrSrc)

function buildPermBlock(categories, lang) {
  const perm = {}
  for (const cat of categories) {
    for (const p of cat.permissions) {
      const k = permNodeKey(p.node)
      if (lang === 'en') {
        perm[k] = { label: p.label, desc: p.desc }
      } else {
        perm[k] = { label: p.label, desc: p.desc } // placeholder; vi file edited below
      }
    }
  }
  return perm
}

